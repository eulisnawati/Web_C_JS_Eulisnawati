// Mendefinisikan jenis barang
let jenisBarang = "Elektronik";

// Menggunakan switch case untuk menentukan kategori barang
switch (jenisBarang) {
  case "Buku":
    console.log("Ini adalah barang kategori Buku.");
    break;
  case "Elektronik":
    console.log("Ini adalah barang kategori Elektronik.");
    break;
  case "Pakaian":
    console.log("Ini adalah barang kategori Pakaian.");
    break;
  case "Mainan":
    console.log("Ini adalah barang kategori Mainan.");
    break;
  default:
    console.log("Kategori barang tidak dikenali.");
}
