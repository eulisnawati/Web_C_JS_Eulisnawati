let sakitHati = true; // Jika sakitHati adalah true, seseorang merasa sakit hati.

if (sakitHati) {
  // Jika sakitHati adalah true, seseorang merasa sakit hati.
  console.log("Saya merasa sakit hati. Mungkin saya perlu berbicara dengan seseorang tentang perasaan saya.");
} else {
  // Jika sakitHati adalah false, seseorang tidak merasa sakit hati.
  console.log("Saya tidak merasa sakit hati saat ini. Semoga suasana hati saya baik-baik saja.");
}
